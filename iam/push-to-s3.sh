#!/bin/bash

FUNCTION_BUCKET="dumpzonehg"
FUCTION_BUCKET_FOLDER="pylambda"
BASIC_POLICY_NAME="basic_policy.json"
ACCESS_POLICY_NAME="access_policy.json"
TRUST_POLICY_NAME="trust_policy.json"
BASIC_POLICY_S3_URL="s3://${FUNCTION_BUCKET}/${FUCTION_BUCKET_FOLDER}/${BASIC_POLICY_NAME}"
ACCESS_POLICY_S3_URL="s3://${FUNCTION_BUCKET}/${FUCTION_BUCKET_FOLDER}/${ACCESS_POLICY_NAME}"
TRUST_POLICY_S3_URL="s3://${FUNCTION_BUCKET}/${FUCTION_BUCKET_FOLDER}/${TRUST_POLICY_NAME}"

## ColorVars
RED='\033[0;31m'
GREEN='\033[0;32m'
NC='\033[0m'

echo -e "${GREEN}Pushing Basic Policy to ${BASIC_POLICY_S3_URL}${NC}"
aws s3 cp $BASIC_POLICY_NAME $BASIC_POLICY_S3_URL
echo -e "${GREEN}Pushing Access Policy to ${ACCESS_POLICY_S3_URL}${NC}"
aws s3 cp $ACCESS_POLICY_NAME $ACCESS_POLICY_S3_URL
echo -e "${GREEN}Pushing Trust Policy to ${TRUST_POLICY_S3_URL}${NC}"
aws s3 cp $TRUST_POLICY_NAME $TRUST_POLICY_S3_URL
